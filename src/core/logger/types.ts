/**
 * 系统日志类型定义
 *
 * 定义面向开发者的分级日志系统的核心类型：
 * LogLevel 枚举和 Logger 接口。
 *
 * @module core/logger
 */

// ============================================
// 日志级别
// ============================================

/** 日志级别（数值越小越详细） */
export enum LogLevel {
  /** 调试信息，最详细 */
  DEBUG = 0,
  /** 一般信息 */
  INFO = 1,
  /** 警告 */
  WARN = 2,
  /** 错误 */
  ERROR = 3,
  /** 静默，不输出任何日志 */
  SILENT = 4,
}

// ============================================
// Logger 接口
// ============================================

/** 日志记录函数签名 */
export type LogFn = (message: string, ...args: unknown[]) => void;

/**
 * 日志记录器接口
 *
 * 每个 Logger 实例绑定一个模块命名空间，
 * 所有日志输出自动添加 `[name]` 前缀。
 */
export interface Logger {
  /** 调试日志 */
  debug: LogFn;
  /** 信息日志 */
  info: LogFn;
  /** 警告日志 */
  warn: LogFn;
  /** 错误日志 */
  error: LogFn;
}
