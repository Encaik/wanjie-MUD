/**
 * 获取功法名称
 */
export function getTechniqueName(type: TechniqueType, rarity: ItemRarity): string {
  const names = TECHNIQUE_NAMES[type][rarity];
  return names[Math.floor(Math.random() * names.length)];
}

/**
 * 获取功法描述
 */
export function getTechniqueDescription(type: TechniqueType, rarity: ItemRarity): string {
  const descriptions = TECHNIQUE_DESCRIPTIONS[type][rarity];
  return descriptions[Math.floor(Math.random() * descriptions.length)];
}
