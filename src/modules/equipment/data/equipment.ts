/**
 * 获取装备名称
 */
export function getEquipmentName(worldType: WorldType, slot: EquipmentSlot, qualityIndex: number = 0): string {
  const names = EQUIPMENT_NAMES[worldType]?.[slot] || EQUIPMENT_NAMES['修仙'][slot];
  const index = Math.min(qualityIndex, names.length - 1);
  return names[index];
}

/**
 * 根据索引获取所有槽位的装备名称
 */
export function getEquipmentNameByIndex(worldType: WorldType, slot: EquipmentSlot, index: number): string {
  const names = EQUIPMENT_NAMES[worldType]?.[slot] || EQUIPMENT_NAMES['修仙'][slot];
  const safeIndex = Math.abs(index) % names.length;
  return names[safeIndex];
}
